function getName()
{  
  $file = fopen ("count.txt","r+");  
  if (!$file)
  {
    echo("file cant be open");
  }
  else
  {
  	
  	$count = fread($file, 5);
  	$count++;
  	
  }  
  fclose ($file);
  
  
  $file = fopen ("count.txt","w+");
  if ( !$file )
  {
    echo("file cant be open");
  }
  else
  {
    fputs ($file, $count);
  }  
  fclose ($file);
  return $count;    
}

